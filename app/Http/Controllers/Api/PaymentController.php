     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $payment = new Payment();
        $payment->cardNumber = $request->payment_number;
        $payment->mois = $request->payment_mois;
        $payment->year = $request->payment_year;
        $payment->cvCode = $request->payment_cvCode;
        $payment->address = $request->payment_adress;
        $payment->save();
    }

